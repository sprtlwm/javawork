package p06class.p02quiz.p01static;

public class Quiz02 {

	public static void main(String[] args) {
	
		//정수(int)로 표현할 수 있는 최대값/최소값
		// 을 api에서 integer 클래스를 찾아서
		// 관련된 필드를 사용하여 출력
		
	System.out.println("최대값:" + Integer.MAX_VALUE);
	System.out.println("최소값:" + Integer.MIN_VALUE);

	//정수(long) 최대값 / 최소값
	
	System.out.println("long 최대값:" + Long.MAX_VALUE);
	System.out.println("long 최소값:" + Long.MIN_VALUE);
	}
}
